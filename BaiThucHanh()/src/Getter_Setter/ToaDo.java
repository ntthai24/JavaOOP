package Getter_Setter;

public class ToaDo {
    private int x;
    private int y;
    
    //getter for x
    public int getX()
    {
        return x;
    }

    //setter for x
    public void setX(int x)
    {
        this.x = x;
    }

    //getter for y
    public int getY()
    {
        return y;
    }

    //setter for y
    public void setY(int y)
    {
        this.y = y;
    }
}
